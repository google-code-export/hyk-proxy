    private void deployButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deployButtonActionPerformed
        Misc.getGlobalThreadPool().submit(new Runnable() {

            public void run() {
                deployButton.setEnabled(false);
                SwingUtilities.getWindowAncestor(GAEConfigPanel.this).setAlwaysOnTop(false);
                try {
                    final File home = GAEPlugin.getHome();
                    File serverDeployHome = new File(home, "deploy/war");
                    if (!serverDeployHome.exists()) {
                        Future<Boolean> future = Misc.getGlobalThreadPool().submit(new Callable<Boolean>() {

                            public Boolean call() throws Exception {
                                try {
                                    URL serverPackageUrl = new URL(Constants.SERVER_PACKAGE_URL);
                                    File deploy = new File(home, "deploy");
                                    File file = NetUtil.downloadFile(serverPackageUrl, deploy);
                                    if (null != file) {
                                        FileUtil.unzip(file, deploy, true);
                                    }
                                    return Boolean.TRUE;
                                } catch (Exception e) {
                                    logger.error("Failed to download gae server package!", e);
                                }
                                return Boolean.FALSE;
                            }
                        });
                        SwingHelper.showBusyButton(future, deployButton, "Downloading...");
                        try {
                            if (!future.get()) {
                                deployButton.setText("Error!");
                                deployButton.setEnabled(false);
                                JOptionPane.showMessageDialog(null, "Faield to download server package, please refer log file for more details.");
                                return;
                            } else {
                                SwingUtilities.getWindowAncestor(GAEConfigPanel.this).dispose();
                            }
                        } catch (Exception e) {
                        }
                    }
                    Future future = Misc.getGlobalThreadPool().submit(new Runnable() {

                        public void run() {
                            try {
                                String filesp = System.getProperty("file.separator");
                                String appcfgwrapperjar = home.getAbsolutePath() + filesp + "deploy" + filesp + "AppCfgWrapper" + filesp + "AppCfgWrapper.jar";
                                StringBuilder cmd = new StringBuilder();
                                cmd.append("java ");
                                String warPath = home.getAbsolutePath() + filesp + "deploy" + filesp + "war";
                                cmd.append("-Dappengine.app.location=").append(warPath).append(" ");
                                int row = appidTable.getSelectedRow();
                                if (row >= 0) {
                                    String appid = (String) appIdTableModel.getValueAt(row, 0);
                                    cmd.append("-Dappengine.appid=").append(appid).append(" ");
                                }
                                cmd.append(" -jar \"").append(appcfgwrapperjar).append("\"");
                                //ProcessBuilder pb = new ProcessBuilder(cmd.toString());
                                Process p = Runtime.getRuntime().exec(cmd.toString());
                                p.waitFor();
                            } catch (Exception ex) {
                                logger.error("", ex);
                            }
                        }
                    });
                    SwingHelper.showBusyButton(future, deployButton, "Deploying...");
                } finally {
                    deployButton.setEnabled(true);
                    deployButton.setIcon(GAEImageUtil.APPENGINE16);
                    deployButton.setText("Deploy Server");
                    SwingUtilities.getWindowAncestor(GAEConfigPanel.this).setAlwaysOnTop(true);
                }


            }
        });

    }//GEN-LAST:event_deployButtonActionPerformed
