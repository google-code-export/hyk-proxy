    private void loadDefaultValueFromConfig() {
        appids.clear();
        Config config = Config.getInstance();
        List<HykProxyServerAuth> appids = config.getHykProxyServerAuths();
        while (appIdTableModel.getRowCount() > 0) {
            appIdTableModel.removeRow(0);
        }
        for (HykProxyServerAuth appid : appids) {
            appIdTableModel.addRow(new Object[]{appid.appid});
            this.appids.put(appid.appid, appid);
        }

        if (config.getClient2ServerConnectionMode().equals(ConnectionMode.XMPP2GAE)) {
            isXMPPEnable.setSelected(true);
            while (xmppTableModel.getRowCount() > 0) {
                xmppTableModel.removeRow(0);
            }
            List<XmppAccount> accounts = config.getXmppAccounts();
            for (XmppAccount account : accounts) {
                xmppTableModel.addRow(new Object[]{account.jid});
            }
        } else {
            disableXmpp();
        }

        if (null != config.getHykProxyClientLocalProxy()) {
            ProxyInfo info = config.getHykProxyClientLocalProxy();
            isHttpProxyEnable.setSelected(true);
            localHttpProxyHostText.setText(info.host);
            localHttpProxyPortText.setText(info.port + "");
            localHttpProxyUserText.setText(info.user);
            localHttpProxyPassText.setText(info.passwd);
        } else {
            isHttpProxyEnable.setSelected(false);
            disableHttpProxy();
        }

        rpcTimeoutText.setText(config.getRpcTimeOut() + "");
        connectionPoolText.setText(config.getHttpConnectionPoolSize() + "");
        fetcherNumText.setText(config.getMaxFetcherNumber() + "");
        compressorType.setSelectedItem(config.getCompressor());
        encrypterCombox.setSelectedItem(config.getHttpUpStreamEncrypter());
        fetchLimitTextField1.setText(config.getFetchLimitSize() + "");
        isHttpSimpleUrlEnable.setSelected(config.isSimpleURLEnable());
        setVisible(true);
    }

    protected Config buildConfProps() throws Exception {
        Config config = Config.getInstance();
        Properties props = new Properties();
        List<HykProxyServerAuth> list = new ArrayList<HykProxyServerAuth>(appids.values());
        config.setHykProxyServerAuths(list);
        List<XmppAccount> xmppAccountList = new ArrayList<XmppAccount>(xmppAccounts.values());
        config.setXmppAccounts(xmppAccountList);

        if (isXMPPEnable.isSelected()) {
            config.setClient2ServerConnectionMode(ConnectionMode.XMPP2GAE);
        } else {
            config.setClient2ServerConnectionMode(ConnectionMode.HTTP2GAE);
        }

        if (isHttpProxyEnable.isSelected()) {
            ProxyInfo localProxy = new ProxyInfo();
            localProxy.host = localHttpProxyHostText.getText().trim();
            localProxy.port = Integer.parseInt(localHttpProxyPortText.getText().trim());
            localProxy.user = localHttpProxyUserText.getText().trim();
            localProxy.passwd = new String(localHttpProxyPassText.getPassword()).trim();
            config.setHykProxyClientLocalProxy(localProxy);
        } else {
            config.setHykProxyClientLocalProxy(null);
        }
        config.setRpcTimeOut(Integer.parseInt(rpcTimeoutText.getText().trim()));
        config.setSimpleURLEnable(isHttpSimpleUrlEnable.isSelected());
        config.setFetchLimitSize(Integer.parseInt(fetchLimitTextField1.getText().trim()));
        config.setHttpConnectionPoolSize(Integer.parseInt(connectionPoolText.getText().trim()));
        config.setCompressor(compressorType.getSelectedItem().toString());
        config.setMaxFetcherNumber(Integer.parseInt(fetcherNumText.getText().trim()));
        //config.setHttpUpStreamEncrypter(encrypterCombox.getSelectedItem().toString());
        return config;
    }

